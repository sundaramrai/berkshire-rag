export function getBerkshireLetterSourceLink(label: string | undefined) {
  const year = label ? /(?:19|20)\d{2}/.exec(label)?.[0] : undefined;

  return year ? BERKSHIRE_LETTER_SOURCE_LINKS[year] : undefined;
}

export function getBerkshireLetterYearsFromText(text: string) {
  const years = new Set<string>();
  const yearPattern = /(?:19|20)\d{2}/g;
  let match = yearPattern.exec(text);

  while (match) {
    const year = match[0];

    if (BERKSHIRE_LETTER_SOURCE_LINKS[year]) {
      years.add(year);
    }

    match = yearPattern.exec(text);
  }

  return [...years];
}
